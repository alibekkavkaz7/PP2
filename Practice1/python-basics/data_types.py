# Example 1: integer type
age = 19
print(type(age))

# Example 2: float type
temperature = 36.6
print(type(temperature))

# Example 3: string type
country = "Kazakhstan"
print(type(country))

# Example 4: boolean type
is_student = True
print(type(is_student))

# Example 5: list type
subjects = ["Calculus", "Linear Algebra", "PP2"]
print(type(subjects))
