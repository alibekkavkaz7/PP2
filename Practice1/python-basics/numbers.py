# Example 1: integer operations
a = 12
b = 5
print(a + b)
print(a - b)
print(a * b)
print(a / b)

# Example 2: power
x = 3
print(x ** 2)

# Example 3: float numbers
price = 9.75
quantity = 4
total = price * quantity
